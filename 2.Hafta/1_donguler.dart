void main(List<String> args) {
  //loops => döngüler

  for (var i = 0; i <= 15; i++) {
    print(i);
  }

  print("****************************");

  int sayac = 1;

  while (sayac <= 15) {
    print(sayac);
    sayac++;
  }

  //infinite loops => sonsuz döngüler
  // continue =>alt satırları çalıştırmaz döngü başına döner
  //break => döngüden tamamen çıkar
}
